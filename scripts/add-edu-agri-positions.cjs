let pid = 4000;
let added = 0;
const batch = [];

function P(officeId, design, grade, parentId, equiv, salutation, appointing, usecase, progression, resp) {
  pid++;
  added++;
  return {
    id: `pos-${pid}`, office_id: officeId, designation_bn: design, pay_grade_bn: grade,
    parent_position_id: parentId || null, equivalent_rank_bn: equiv || '',
    official_salutation_bn: salutation || '', appointing_authority_bn: appointing || '',
    public_service_usecase_bn: usecase || '', career_progression_bn: progression || '',
    responsibilities_bn: resp || ''
  };
}

const idMap = {};
function saveId(key) { idMap[key] = `pos-${pid}`; }

function addHierarchy(officeId, bossDesig, bossGrade, dirs, startPid) {
  const boss = P(officeId, bossDesig, bossGrade, null, '', '', '', '', '', '');
  if (boss) { saveId(`h_${officeId}_boss`); }
  if (!dirs || !bossDesig) return;
  dirs.forEach((d, i) => {
    const p = P(officeId, d.desig, d.grade, boss ? boss.id : null, d.equiv||'', d.sal||'', d.appointing||'', d.usecase||'', d.prog||'', d.resp||'');
  });
}

