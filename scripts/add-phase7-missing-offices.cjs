const fs = require('fs');
const path = require('path');

const DATA = path.join(__dirname, '..', 'src', 'data');
const offices = JSON.parse(fs.readFileSync(path.join(DATA, 'offices.json'), 'utf8'));
const positions = JSON.parse(fs.readFileSync(path.join(DATA, 'positions.json'), 'utf8'));

let maxNum = 0;
positions.forEach(p => {
  const match = p.id.match(/p-(\d+)/);
  if (match) maxNum = Math.max(maxNum, parseInt(match[1]));
});
let nextPosId = maxNum + 1;
function genPosId() { return `p-${nextPosId++}`; }

function makePos(officeId, desig, grade, parentId, qual, appt, resp) {
  return {
    id: genPosId(), office_id: officeId, designation_bn: desig, pay_grade_bn: grade,
    parent_position_id: parentId || null, subordinate_position_ids: [],
    equivalent_rank_bn: '', official_salutation_bn: '', appointing_authority_bn: '',
    public_service_usecase_bn: resp || '', career_progression_bn: resp || '',
    responsibilities_bn: resp || '', qualifications_bn: qual || '', appointment_process_bn: appt || '',
  };
}

function addOffice(id, name, parentId, type, jurisdiction, law, audience, desc) {
  if (offices.find(o => o.id === id)) { console.log(`  SKIP: ${name}`); return false; }
  offices.push({
    id, name_bn: name, parent_office_id: parentId, office_type_bn: type,
    jurisdiction_bn: jurisdiction, governing_law_bn: law, target_audience_bn: audience,
    category: 'Executive', contact_info: { website: '', phone: '' }, description_bn: desc,
  });
  return true;
}

function addChain(officeId, chain) {
  const newPositions = []; let prevParent = null;
  chain.forEach(item => {
    const pos = makePos(officeId, item.desig, item.grade, prevParent ? prevParent.id : null, item.qual, item.appt, item.resp);
    newPositions.push(pos); prevParent = pos;
  });
  positions.push(...newPositions);
  return newPositions.length;
}

