// Helper
function enhanceGeneric(officeId, hierarchy) {
  const existing = positions.filter(p => p.office_id === officeId);
  if (existing.length !== 2) return false;
  
  const topExisting = { head: existing[0], second: existing[1] };
  const newOnes = [];
  const allPositions = [...existing];
  
  hierarchy.forEach((item, idx) => {
    let parentId = null;
    // Try to find parent in existing or newly created
    if (item.parent) {
      let parent = existing.find(p => p.designation_bn === item.parent);
      if (!parent) parent = newOnes.find(p => p.designation_bn === item.parent);
      if (parent) parentId = parent.id;
    }
    // If no explicit parent, use SECOND position as default
    if (!parentId && idx === 0 && topExisting.second) {
      parentId = topExisting.second.id;
    }
    // Chain to previous in this array
    if (!parentId && idx > 0 && newOnes[idx - 1]) {
      parentId = newOnes[idx - 1].id;
    }
    
    const p = makePos(officeId, item.desig, item.grade, parentId, item.equiv || '', item.appAuth || '',
      item.resp || '', item.qual || '', item.appt || '');
    newOnes.push(p);
    allPositions.push(p);
  });
  
  register(newOnes);
  return newOnes.length;
}

