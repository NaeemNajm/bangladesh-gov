  const office = officeMap[p.office_id];
  if (!office) return;
  const sameOffice = positions.filter(x => x.office_id === p.office_id);

  // Find the head of this office (the one with parent=null who is the most senior)
  const head = sameOffice.filter(x => !x.parent_position_id).sort((a, b) => {
    const gradeOrder = { 'গ্রেড ১':1,'গ্রেড ২':2,'গ্রেড ৩':3,'গ্রেড ৪':4,'গ্রেড ৫':5,'গ্রেড ৬':6,'গ্রেড ৭':7,'গ্রেড ৮':8,'গ্রেড ৯':9,'গ্রেড ১০':10,'গ্রেড ১১':11,'গ্রেড ১২':12,'গ্রেড ১৩':13,'গ্রেড ১৪':14,'গ্রেড ১৫':15,'গ্রেড ১৬':16 };
    const ag = gradeOrder[a.pay_grade_bn] || 99;
    const bg = gradeOrder[b.pay_grade_bn] || 99;
    return ag - bg;
  })[0];

  if (!head || head.id === p.id) return;

  const d = p.designation_bn;

